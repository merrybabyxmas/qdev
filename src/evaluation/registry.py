@dataclass(frozen=True)
class ExperimentSpec:
    pipeline_id: str
    name: str
    family: str
    doc_status: str
    implementation_mode: str
    base_model: str
    feature_profile: str
    allocation_mode: str = "equal_positive"
    overlays: tuple[str, ...] = ()
    risk_cap: float = 0.40
    max_drawdown: float = 0.20
    source_doc: str = ""
    note: str = ""

    def feature_columns(self, frame: pd.DataFrame | None = None) -> list[str]:
        columns = list(FEATURE_PROFILES[self.feature_profile])
        if frame is None:
            return columns
        return [column for column in columns if column in frame.columns]


def _spec(
    pipeline_id: str,
    name: str,
    family: str,
    doc_status: str,
    implementation_mode: str,
    base_model: str,
    feature_profile: str,
    *,
    allocation_mode: str = "equal_positive",
    overlays: tuple[str, ...] = (),
    risk_cap: float = 0.40,
    max_drawdown: float = 0.20,
    source_doc: str = "",
    note: str = "",
) -> ExperimentSpec:
    return ExperimentSpec(
        pipeline_id=pipeline_id,
        name=name,
        family=family,
        doc_status=doc_status,
        implementation_mode=implementation_mode,
        base_model=base_model,
        feature_profile=feature_profile,
        allocation_mode=allocation_mode,
        overlays=overlays,
        risk_cap=risk_cap,
        max_drawdown=max_drawdown,
        source_doc=source_doc,
        note=note,
    )


